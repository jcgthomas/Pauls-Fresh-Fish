<p>
Paul’s Fresh Fish is a family-run fishmongers, providing premium fish and seafood sourced from British Coastlines, with a few exotic favourites also on offer.
</p>
<p>
Based in Gloucestershire, our small team visit wholesale markets daily to hand pick fresh fish. Our main focus is selling sustainably sourced fresh fish at our local markets in Stroud, Witney (Oxfordshire), Leominster, Hereford and Gloucester. We also provide a home delivery service.
</p>
<p>
We offer a huge variety of British-caught fish - regularly stocking whole hake, gunards, turbot and squid - as well as the classic favourites: salmon, cod, haddock and plaice. All of our fish are available to buy filleted. 
</p>
<img class="aboutBanner banner1" src="/resources/images/banner2.jpg" alt="Fresh fish display at the market" srcset="">
<p>
You will also find a range of handmade gluten and dairy-free fish cakes, mackerel and salmon pates, pickled herrings, and taramasalata (when available) on sale at our market stall.
</p>
